    private void SearchjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchjButtonActionPerformed
       Date date = new Date();
       String urls=SearchjTextArea.getText();
       if(urls.length()==0)
       {
        JOptionPane.showMessageDialog(null, "Enter any text please!", "Text Not Entered!", JOptionPane.ERROR_MESSAGE); 
        return;
       }
           
       String csvFile="";
       if(path!=null)
       {
           csvFile =path+"\\"+date.getTime()+".csv";
       }else
       {
           JOptionPane.showMessageDialog(null, "You have not chose any folder\nwhere file will save.Please Select any folder!", "Folder Not Chosen!", JOptionPane.ERROR_MESSAGE); 
           return;
       }
       
       JOptionPane.showMessageDialog(null,"Searching Started!\nWait Until Next Not Message Show.","Searching...",  JOptionPane.INFORMATION_MESSAGE);
        FileWriter writer = null;
        try
        {
            writer = new FileWriter(csvFile);
            writer.append("Keywords,\"Results\"\n");
        }catch(IOException e)
        {
            JOptionPane.showMessageDialog(null, e, "File Error", JOptionPane.ERROR_MESSAGE);    
        }
        CsvUtils csvUtils=new CsvUtils();
        yahSear=new YahooSearch();
        String[] splitStr = urls.split("\\n");
        int size=splitStr.length,i=1;
        for(String singleStr: splitStr)
        {
            SearchingjProgressBar.setValue((i/size)*100);
            try{
                htmlText=yahSear.search(singleStr);
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, "Internet Connrction Error!", "Connection Fail", JOptionPane.ERROR_MESSAGE);
                return;
            }
           try { 
               writer.append(singleStr+",\""+csvUtils.writeLine(execuator(htmlText))+"\"\n");
              
           } catch (IOException e) {
               JOptionPane.showMessageDialog(null, e, "File writing Error", JOptionPane.ERROR_MESSAGE);    
           }
            i++;
            
        }
        try{
            writer.flush();
            writer.close();
        } catch (IOException e) {
               JOptionPane.showMessageDialog(null, e, "File closing Error", JOptionPane.ERROR_MESSAGE);    
           }
        
        JOptionPane.showMessageDialog(null,"Searching Completed & File Created","Done",  JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_SearchjButtonActionPerformed
