    private String execuator(String htmlString){
        Document doc = kit.createDefaultDocument();
              jEditorPane.setDocument(doc);
              jEditorPane.setText(htmlString);
              jEditorPane.selectAll();
              plainText=jEditorPane.getSelectedText();
              return plainText;
    }
